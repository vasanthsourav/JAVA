public class ifelse {
    public static void main(String[] args)
    {
        int score = 60;
        if (score >= 70)
        {
            System.out.println("Pass");
        }
        else if (score == 60)
        {
            System.out.println("Just Pass");
        }
        else
        {
            System.out.println("Fail");
        }
    }
    
}
