public class SwitchCase {
    
    public static void main(String[] args) {
        int switchvalue =82;
        switch(switchvalue) {
            case 1:
            System.out.println("Value was 1");
            break;
            case 8:
            System.out.println("Value was 8");
            break;
            default:
            System.out.println("Was not 1 or 8");
            break;
    }
}
}